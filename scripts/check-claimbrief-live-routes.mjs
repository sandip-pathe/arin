import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const campaignDate = "2026-07-02";
const generatedDir = join(root, "docs", "outreach", "generated");
const publicBaseUrl = process.env.CLAIMBRIEF_PUBLIC_BASE_URL || "https://app.anaya.legal";
const strict = process.argv.includes("--strict");

const routes = [
  {
    name: "ClaimBrief landing",
    url: `${publicBaseUrl}/claimbrief`,
    required: true,
    expectedText: "Cited property-claim review briefs",
  },
  {
    name: "Starter close",
    url: `${publicBaseUrl}/claimbrief/starter`,
    required: true,
    expectedText: "Start with 3 ClaimBriefs for $99",
  },
  {
    name: "Sample packet",
    url: `${publicBaseUrl}/samples/claimbrief-sample-review.html`,
    required: true,
    expectedText: "ClaimBrief",
  },
  {
    name: "Oklahoma wind/hail page",
    url: `${publicBaseUrl}/claimbrief/oklahoma-hail`,
    required: false,
    expectedText: "Turn one Oklahoma wind/hail file into a cited review brief",
  },
];

const checkRoute = async (route) => {
  const started = Date.now();
  try {
    const response = await fetch(route.url, { redirect: "follow" });
    const text = await response.text();
    const expectedTextFound = route.expectedText ? text.includes(route.expectedText) : true;
    return {
      ...route,
      ok: response.ok && expectedTextFound,
      status: response.status,
      expectedTextFound,
      ms: Date.now() - started,
      error: "",
    };
  } catch (error) {
    return {
      ...route,
      ok: false,
      status: "error",
      expectedTextFound: false,
      ms: Date.now() - started,
      error: error instanceof Error ? error.message : String(error),
    };
  }
};

const results = await Promise.all(routes.map(checkRoute));
const requiredFailures = results.filter((result) => result.required && !result.ok);
const optionalFailures = results.filter((result) => !result.required && !result.ok);
const status = requiredFailures.length ? "BLOCKED" : optionalFailures.length ? "READY_WITH_WARNINGS" : "READY";

const report = `# ClaimBrief Live Route Check - ${campaignDate}

Status: **${status}**

Generated by \`npm run outreach:claimbrief:live-check\`.

Base URL: ${publicBaseUrl}

## Routes

| Route | Required | Result | Status | Expected Text | Time |
| --- | --- | --- | ---: | --- | ---: |
${results
  .map(
    (result) =>
      `| ${result.name} | ${result.required ? "yes" : "no"} | ${
        result.ok ? "pass" : "fail"
      } | ${result.status} | ${result.expectedTextFound ? "found" : "missing"} | ${result.ms}ms |`
  )
  .join("\n")}

## Notes

${
  optionalFailures.length
    ? "- Optional trigger pages can be used locally or after deploy, but generated outreach should link to a live required route until they pass."
    : "- Optional trigger pages are live."
}
${requiredFailures.length ? "- Required outreach URLs are not all live; do not send external outreach yet." : "- Required outreach URLs are live."}
`;

mkdirSync(generatedDir, { recursive: true });
const reportPath = join(generatedDir, `claimbrief-live-routes-${campaignDate}.md`);
writeFileSync(reportPath, report);

console.log(`ClaimBrief live route check: ${status}`);
console.log(`Report: ${reportPath}`);
for (const result of results) {
  console.log(
    `${result.ok ? "PASS" : "FAIL"} ${result.status} ${result.name} ${result.url}`
  );
  if (result.error) {
    console.log(`  ${result.error}`);
  }
}

if (strict && requiredFailures.length) {
  process.exitCode = 1;
}
